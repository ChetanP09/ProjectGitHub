package log2;

public class SwapUsing3rd {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int temp;
		System.out.println("First Number--->"+a);
		System.out.println("Second Number--->"+b);
		
		temp=a;
		a=b;
		b=temp;
		System.out.println("After Swapping");
		
		System.out.println("First Number--->"+a);
		System.out.println("Second Number--->"+b);
		

}
}
