package logicalProgram;

public class Swap2 
{
	//swap using 3rd variable
	
	public static void main(String[] args) 
	{
		int a=100;
		int b=200;
		int c;
		System.out.println("First number is--> "+a);
	    System.out.println("Second number is--> "+b);
	    c=a;
	    a=b;
	    b=c;
	    System.out.println("After Swapping");
	    System.out.println("First number is--> "+a);
	    System.out.println("Second number is--> "+b);
	

}
}
